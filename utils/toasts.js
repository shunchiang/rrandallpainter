import { toast } from "react-toastify";

export const successToast = (msg) =>
  toast.success(msg, {
    position: "top-right",
    autoClose: 2000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
    className: style.toastSuccess,
  });

